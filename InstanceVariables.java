package com.mypackage;

public class InstanceVariables {
	int i=3;
	String s="Sai";
	int salary=3000;
	
	public static void main(String[] args) {
		InstanceVariables i=new InstanceVariables();
		
		System.out.println(i.i);
		System.out.println(i.s);
		System.out.println(i.salary);
	}

}
